# Plotting the histogram for colors in the image
for i, col in enumerate(['r', 'g', 'b']):
    hist = cv2.calcHist([img], [i], None, [256], [0, 256])
    plt.subplot(224)
    plt.title('Color Histogram')
    plt.plot(hist, color = col)
    plt.xlim([0, 256])