# Reading the image
img = cv2.imread("/content/Flowers.jpg")
img = cv2.cvtColor(img, cv2.COLOR_BGR2RGB)
img_1 = cv2.cvtColor(img, cv2.COLOR_BGR2GRAY)

# Setting the grid size
plt.figure(figsize=(20,20))

# Plotting the original image
